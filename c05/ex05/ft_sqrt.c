int		ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (nb > 0)
	{
		while (i * i < nb && i < 46341)
			i++;
		if (i * i ==  nb)
			return (i);
		else
			return (0);
	}
	else
		return (0);
}
