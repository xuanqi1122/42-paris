char	*ft_strcat(char *dest, char *src)
{
	int i;
	int ld;

	i = 0;
	ld = 0;
	while (dest[ld])
		ld++;
	while (src[i])
	{
		dest[ld + i] = src[i];
		i++;
	}
	dest[ld + i] = '\0';
	return (dest);
}
