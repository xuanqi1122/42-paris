#include <unistd.h>

int		main(int ac, char **av)
{
	int i;
	int j;
	int k;

	i = 0;
	j = 0;
	k = 0;
	while (av[2][j])
		j++;
	while (av[3][k])
		k++;
	if (ac != 4 || j > 1 || k > 1)
	{
		write (1, "\n", 1);
		return (0);
	}
	while (av[1][i] && av[2][0] && av[3][0])
	{
		if (av[1][i] == av[2][0])
			av[1][i] = av[3][0];
		write(1, &av[1][i], 1);
		i++;
	}
	return (0);
}
