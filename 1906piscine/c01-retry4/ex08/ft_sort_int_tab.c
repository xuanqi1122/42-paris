void	ft_sort_int_tab(int *tab, int size)
{
	int t;
	int pro;

	t = 0;
	while (t < size - 1)
	{
		if (tab[t] > tab[t + 1])
		{
			pro = tab[t];
			tab[t] = tab[t + 1];
			tab[t + 1] = pro;
			if (t > 0)
				t = t - 2;
		}
		t++;
	}
}
